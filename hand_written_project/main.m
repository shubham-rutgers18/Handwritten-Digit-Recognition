% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
try
    userImage=handles.userImage;
    [fGray, fBinary, fCrop, fResize] = loadUserImage(userImage);
    axes(handles.axes2);
    imshow(fGray);
    axes(handles.axes3);
    imshow(fBinary);
    axes(handles.axes4);
    imshow(fCrop);
    axes(handles.axes5);
    imshow(fResize);
    handles.fResize = fResize;
    guidata(hObject, handles);
catch
    uiwait(warndlg('Please input an image first!!'));  % Popup message alert to user
end

